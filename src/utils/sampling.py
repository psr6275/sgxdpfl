def load_dataset_info(args, data_dir = '../data/examples'):

    dirname = os.path.join(data_dir, args.dataset, "users_%s_iid_%s"%(args.num_users,args.iid))
    if os.path.isdir(dirname):
        print("%s already exists"%dirname)
    else:
        os.makedirs(dirname)

    try:
        data_dic = torch.load(os.path.join(dirname, "data_info.pth"))
        # dic_users = data_dic['dic_users']
        # dataset_test = data_dic['dataset_test']
        # args.num_channels = data_dic['num_channels']
        for ui in range(data_dic['num_users']):
            if not os.path.exists(os.path.join(dirname, "dataset_id_%s.pth"%ui)):
                print("there is not file for user %s in %s"%(ui, args.dataset))
        return data_dic

    except:
        print("we need to create %s datasets for %s users in iid setting %s"%(args.dataset,args.num_users, args.iid))
    
    if args.dataset == 'mnist':
        trans_mnist = transforms.Compose([transforms.ToTensor(), transforms.Normalize((0.1307,), (0.3081,))])
        dataset_train = datasets.MNIST(os.path.join(data_dir, args.dataset), train=True, download=True, transform=trans_mnist)
        dataset_test = datasets.MNIST(os.path.join(data_dir, args.dataset), train=False, download=True, transform=trans_mnist)
        args.num_channels = 1
        # sample users
        if args.iid:
            dict_users = mnist_iid(dataset_train, args.num_users)
        else:
            dict_users = mnist_noniid(dataset_train, args.num_users)
    elif args.dataset == 'cifar':
        #trans_cifar = transforms.Compose([transforms.ToTensor(), transforms.Normalize((0.5, 0.5, 0.5), (0.5, 0.5, 0.5))])
        args.num_channels = 3
        trans_cifar_train = transforms.Compose([
            transforms.RandomCrop(32, padding=4),
            transforms.RandomHorizontalFlip(),
            transforms.ToTensor(),
            transforms.Normalize((0.4914, 0.4822, 0.4465), (0.2023, 0.1994, 0.2010)),
        ])
        trans_cifar_test = transforms.Compose([
            transforms.ToTensor(),
            transforms.Normalize((0.4914, 0.4822, 0.4465), (0.2023, 0.1994, 0.2010)),
        ])
        dataset_train = datasets.CIFAR10(os.path.join(data_dir, args.dataset), train=True, download=True, transform=trans_cifar_train)
        dataset_test = datasets.CIFAR10(os.path.join(data_dir, args.dataset), train=False, download=True, transform=trans_cifar_test)
        if args.iid:
            dict_users = cifar_iid(dataset_train, args.num_users)
        else:
            dict_users = cifar_noniid(dataset_train, args.num_users)
    elif args.dataset == 'fashion-mnist':
        args.num_channels = 1
        trans_fashion_mnist = transforms.Compose([transforms.ToTensor(), transforms.Normalize((0.5,), (0.5,))])
        dataset_train = datasets.FashionMNIST(os.path.join(data_dir, args.dataset), train=True, download=True,
                                              transform=trans_fashion_mnist)
        dataset_test  = datasets.FashionMNIST(os.path.join(data_dir, args.dataset), train=False, download=True,
                                              transform=trans_fashion_mnist)
        if args.iid:
            dict_users = mnist_iid(dataset_train, args.num_users)
        else:
            dict_users = mnist_noniid(dataset_train, args.num_users)
    elif args.dataset == 'femnist':
        args.num_channels = 1
        dataset_train = FEMNIST(train=True)
        dataset_test = FEMNIST(train=False)
        dict_users = dataset_train.get_client_dic()
        args.num_users = len(dict_users)
        if args.iid:
            exit('Error: femnist dataset is naturally non-iid')
        else:
            print("Warning: The femnist dataset is naturally non-iid, you do not need to specify iid or non-iid")
    elif args.dataset == 'shakespeare':
        dataset_train = ShakeSpeare(train=True)
        dataset_test = ShakeSpeare(train=False)
        dict_users = dataset_train.get_client_dic()
        args.num_users = len(dict_users)
        if args.iid:
            exit('Error: ShakeSpeare dataset is naturally non-iid')
        else:
            print("Warning: The ShakeSpeare dataset is naturally non-iid, you do not need to specify iid or non-iid")
    else:
        exit('Error: unrecognized dataset')

    ## Save splitted data    
    for ui in range(args.num_users):
        dset = DatasetSplit(dataset_train, dict_users[ui])
        torch.save(dset, os.path.join(dirname, "dataset_id_%s.pth"%ui))
    ## Save datainfo
    data_dic = {}
    data_dic['dict_users'] = dict_users
    data_dic['num_train'] = len(dataset_train)
    data_dic['num_users'] = args.num_users
    data_dic['dataset_test'] = dataset_test
    data_dic['num_channels'] = args.num_channels
    data_dic['sdatadir'] = dirname
    torch.save(data_dic, os.path.join(dirname, "data_info.pth"))
    
    return data_dic

class DatasetSplit(Dataset):
    def __init__(self, dataset, idxs):
        self.dataset = dataset
        self.idxs = list(idxs)

    def __len__(self):
        return len(self.idxs)

    def __getitem__(self, item):
        image, label = self.dataset[self.idxs[item]]
        return image, label


